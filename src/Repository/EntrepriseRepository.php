{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Entreprise::class);
    }


    public function findWithMaxResult()
    {
        return $this->createQueryBuilder('e')
            ->orderBy('e.updateAt', 'ASC')
            ->setMaxResults(6)
            ->getQuery()
            ->getResult()
